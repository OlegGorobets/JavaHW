package Exercise_23;

public class Woman {
    private String name;
    private int age;
    private String husband;

    public Woman(String name, int age, String husband) {
        this.name = name;
        this.age = age;
        this.husband = husband;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getHusband() {
        return husband;
    }
}
