package Exercise_23;

public class Man {
    private String name;
    private int age;
    private String wife;

    public Man(String name, int age, String wife) {
        this.name = name;
        this.age = age;
        this.wife = wife;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getWife() {
        return wife;
    }
}
