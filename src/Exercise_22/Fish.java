package Exercise_22;

public class Fish {
    private String name;
    private int age;
    private String owner;

    public Fish(String name, int age, String owner) {
        this.name = name;
        this.age = age;
        this.owner = owner;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getOwner() {
        return owner;
    }
}
